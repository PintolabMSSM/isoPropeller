#Merge transcripts with the same exon-exon junction in the same locus
sub merge_transcript{
	my ($temp_num_sample, $temp_sref, $temp_start) = @_;
	if(!defined $temp_start){
		@{$temp_sref} = sort { $a->[0] <=> $b->[0] || $a->[1] <=> $b->[1] } @{$temp_sref};
		$temp_start = 0;
		my $temp_size = @{$temp_sref};
#		print "\tStart merging a list of $temp_size transcripts...\n";
	}
	my $temp_last = $temp_sref->[$temp_start];
	$temp_start++;

	if(@{$temp_last}){
		for(my $i = $temp_start; $i < @{$temp_sref}; $i++){
			my $temp_cur = $temp_sref->[$i];
#			print "\tMerging transcript $i:@{$temp_cur} with $temp_start:@{$temp_last}...\n";
			if(!@{$temp_cur}){
#				print "\tEmpty transcript. Skip...\n";
				next;
			}

			if (($temp_cur->[0] >= $temp_last->[0]) && ($temp_cur->[0] <= $temp_last->[1])){
#				print "\tFound overlap!\n";
				if ($temp_cur->[1] > $temp_last->[1]){
					$temp_last->[1] = $temp_cur->[1];
				}
				my @temp_last_exp = @{$temp_last->[2]};
				my @temp_cur_exp = @{$temp_cur->[2]};
				for(my $j = 0; $j < $temp_num_sample; $j++){
					$temp_last_exp[$j] += $temp_cur_exp[$j];
				}
				@{$temp_last->[2]} = @temp_last_exp;
				my %temp_last_tid = %{$temp_last->[3]};
				my %temp_cur_tid = %{$temp_cur->[3]};
				#my @temp_id = keys %{$temp_last->[3]};
#				print "\tTranscript $temp_start: $temp_last->[0]-$temp_last->[1], @temp_id\n";
				#@temp_id = keys %{$temp_cur->[3]};
#				print "\tTranscript $i: $temp_cur->[0]-$temp_cur->[1], @temp_id\n";
				foreach my $temp_tid (keys %temp_cur_tid){
					unless(exists $temp_last_tid{$temp_tid}){
						$temp_last_tid{$temp_tid} = 1;
					}
				}
				%{$temp_last->[3]} = %temp_last_tid;
				my %temp_last_ends = %{$temp_last->[4]};
				my %temp_cur_ends = %{$temp_cur->[4]};
				foreach my $temp_ends (keys %temp_cur_ends){
					unless(exists $temp_last_ends{$temp_ends}){
						$temp_last_ends{$temp_ends} = 0;
					}
					$temp_last_ends{$temp_ends}++;
				}
				%{$temp_last->[4]} = %temp_last_ends;
#				print "\tClearing the current transcript $i...\n";
				@{$temp_cur} = ();
			}
			else{
				$temp_start = $i;
#				print "\tNo overlap found.\n";
				last;
			}
		}
	}
	if($temp_start < @{$temp_sref}){
#		print "\tSkip to transcript $temp_start as a new start...\n";
		merge_transcript($temp_num_sample, $temp_sref, $temp_start);
	}

	return sort { $a->[0] <=> $b->[0] || $a->[1]<=>$b->[1] } map { @{$_} ? $_ : () } @{$temp_sref};
}

#Compare two transcripts using their exon-exon junction chains and report whether they belong to the same sub-locus
sub compare_transcript{
	my ($temp_transcript_a, $temp_transcript_b) = @_;
	my @temp_transcript_a = split(',', $temp_transcript_a);
	my $temp_num_exon_a = @temp_transcript_a;
	my @temp_transcript_b = split(',', $temp_transcript_b);
	my $temp_num_exon_b = @temp_transcript_b;
	for(my $i = 0; $i < $temp_num_exon_a; $i++){
		my @temp_exon_a = split('-', $temp_transcript_a[$i]);
		for(my $j = 0; $j < $temp_num_exon_b; $j++){
			my @temp_exon_b = split('-', $temp_transcript_b[$j]);
			if(($temp_num_exon_a == 1) || ($temp_num_exon_b == 1)){
				if((($temp_exon_a[0] >= $temp_exon_b[0]) && ($temp_exon_a[0] <= $temp_exon_b[1])) || (($temp_exon_b[0] >= $temp_exon_a[0]) && ($temp_exon_b[0] <= $temp_exon_a[1]))){
					return 1;
				}
			}
			else{
				if(($i == 0) || ($j == 0)){
					if($temp_exon_a[1] == $temp_exon_b[1]){
						return 1;
					}
				}
				elsif(($i == ($temp_num_exon_a - 1)) || ($j == ($temp_num_exon_b - 1))){
					if($temp_exon_a[0] == $temp_exon_b[0]){
						return 1;
					}
				}
				else{
					if(($temp_exon_a[0] == $temp_exon_b[0]) || ($temp_exon_a[1] == $temp_exon_b[1])){
						return 1;
					}
				}
			}
		}
	}

	return 0;
}

#Find the representative transcript in the corresponding sub-locus
sub find_rep_transcript{
	my ($temp_rep_transcript_ref, $temp_query_transcript) = @_;
	if($temp_rep_transcript_ref->{$temp_query_transcript} ne $temp_query_transcript){
		my $temp_rep_transcript = find_rep_transcript($temp_rep_transcript_ref, $temp_rep_transcript_ref->{$temp_query_transcript});

		return $temp_rep_transcript;
	}

	return $temp_query_transcript;
}

#Merge the corresponding sub-loci when a pair of transcripts match
sub merge_sub_loci{
	my ($temp_rep_transcript_ref, $temp_sub_loci_size_ref, $temp_rep_region_ref, $temp_transcript_a, $temp_transcript_b) = @_;
	my $temp_rep_transcript_a = find_rep_transcript($temp_rep_transcript_ref, $temp_transcript_a);
	my $temp_rep_transcript_b = find_rep_transcript($temp_rep_transcript_ref, $temp_transcript_b);
	if($temp_rep_transcript_a eq $temp_rep_transcript_b){

		return 0;
	}
	if($temp_sub_loci_size_ref->{$temp_rep_transcript_a} > $temp_sub_loci_size_ref->{$temp_rep_transcript_b}){
		$temp_rep_transcript_ref->{$temp_rep_transcript_b} = $temp_rep_transcript_a;
		$temp_sub_loci_size_ref->{$temp_rep_transcript_a} += $temp_sub_loci_size_ref->{$temp_rep_transcript_b};
		if($temp_rep_region_ref->{$temp_rep_transcript_b}[0] < $temp_rep_region_ref->{$temp_rep_transcript_a}[0]){
			$temp_rep_region_ref->{$temp_rep_transcript_a}[0] = $temp_rep_region_ref->{$temp_rep_transcript_b}[0];
		}
		if($temp_rep_region_ref->{$temp_rep_transcript_b}[1] > $temp_rep_region_ref->{$temp_rep_transcript_a}[1]){
			$temp_rep_region_ref->{$temp_rep_transcript_a}[1] = $temp_rep_region_ref->{$temp_rep_transcript_b}[1];
		}
	}
	else{
		$temp_rep_transcript_ref->{$temp_rep_transcript_a} = $temp_rep_transcript_b;
		$temp_sub_loci_size_ref->{$temp_rep_transcript_b} += $temp_sub_loci_size_ref->{$temp_rep_transcript_a};
		if($temp_rep_region_ref->{$temp_rep_transcript_a}[0] < $temp_rep_region_ref->{$temp_rep_transcript_b}[0]){
			$temp_rep_region_ref->{$temp_rep_transcript_b}[0] = $temp_rep_region_ref->{$temp_rep_transcript_a}[0];
		}
		if($temp_rep_region_ref->{$temp_rep_transcript_a}[1] > $temp_rep_region_ref->{$temp_rep_transcript_b}[1]){
			$temp_rep_region_ref->{$temp_rep_transcript_b}[1] = $temp_rep_region_ref->{$temp_rep_transcript_a}[1];
		}
	}

	return 1;
}

#Divide a locus into sub-loci
sub locus_sub_cluster{
	my ($temp_locus_ref) = @_;
	my %temp_rep_transcript = ();
	my %temp_sub_loci_size = ();
	my %temp_rep_region = ();
	my %temp_sub_loci = ();
	my %temp_sub_loci_region = ();
	foreach my $temp_eej (sort keys %{$temp_locus_ref->[2]}){
		foreach my $temp_transcript (@{$temp_locus_ref->[2]{$temp_eej}}){
			my $temp_eej_new = $temp_transcript->[0]. $temp_eej . $temp_transcript->[1];
			unless(exists $temp_rep_region{$temp_eej_new}){
				@{$temp_rep_region{$temp_eej_new}} = ($temp_transcript->[0], $temp_transcript->[1]);
			}
			unless(exists $temp_rep_transcript{$temp_eej_new}){
				$temp_rep_transcript{$temp_eej_new} = $temp_eej_new;
			}
			unless(exists $temp_sub_loci_size{$temp_eej_new}){
				$temp_sub_loci_size{$temp_eej_new} = 1;
			}
			unless(exists $temp_sub_loci{$temp_eej_new}){
				$temp_sub_loci{$temp_eej_new} = 0;
			}
		}
	}
	my @temp_transcript = keys %temp_sub_loci;
	my $temp_num_transcript = @temp_transcript;
	for(my $i = 0; $i < ($temp_num_transcript - 1); $i++){
		for(my $j = ($i + 1); $j < $temp_num_transcript; $j++){
			if(compare_transcript($temp_transcript[$i], $temp_transcript[$j])){
				merge_sub_loci(\%temp_rep_transcript, \%temp_sub_loci_size, \%temp_rep_region, $temp_transcript[$i], $temp_transcript[$j]);
			}
		}
	}
	my $temp_sub_loci_count = 0;
	foreach my $temp_transcript (sort {$temp_rep_region{$a}[0] <=> $temp_rep_region{$b}[0]} keys %temp_rep_region){
		if($temp_transcript eq $temp_rep_transcript{$temp_transcript}){
			$temp_sub_loci_count++;
			$temp_sub_loci{$temp_transcript} = $temp_sub_loci_count;
			$temp_sub_loci_region{$temp_sub_loci_count} = $temp_rep_region{$temp_transcript};
			delete $temp_rep_region{$temp_transcript};
		}
	}
	foreach my $temp_transcript (keys %temp_rep_transcript){
		if($temp_transcript ne $temp_rep_transcript{$temp_transcript}){
			$temp_sub_loci{$temp_transcript} = $temp_sub_loci{find_rep_transcript(\%temp_rep_transcript, $temp_transcript)};
			delete $temp_rep_region{$temp_transcript};
		}
	}

	return (\%temp_sub_loci, \%temp_sub_loci_region);
}

#Print the record in gtf format and the expression level
sub print_gtf_exp{
	my ($temp_loci_ref, $temp_sample_ref, $temp_chr, $temp_dir, $temp_in, $temp_exp, $temp_prefix) = @_;
	my %temp_strand_flag = ('+', 0, '-', 1);
	my $temp_exp_fh;
	open(my $temp_gtf_fh, "> ${temp_dir}/${temp_chr}.gtf") or die "<ERROR> Cannot create ${temp_dir}/${temp_chr}.gtf!\n";
	if($temp_exp){
		open($temp_exp_fh, "> ${temp_dir}/${temp_chr}_exp.txt") or die "<ERROR> Cannot create ${temp_dir}/${temp_chr}_exp.txt!\n";
	}
	open(my $temp_id_fh, "> ${temp_dir}/${temp_chr}_id.txt") or die "<ERROR> Cannot create ${temp_dir}/${temp_chr}_id.txt!\n";
	foreach my $temp_strand (keys %$temp_loci_ref){
		my $temp_locus_count = 1;
		foreach my $temp_locus (@{$temp_loci_ref->{$temp_strand}}){
			my ($temp_sub_loci_ref, $temp_sub_loci_region_ref) = locus_sub_cluster($temp_locus);
			#print "$temp_chr\t$temp_strand\t$temp_loci\n";
			foreach my $temp_sub_locus (keys %$temp_sub_loci_region_ref){
				print $temp_gtf_fh "$temp_chr\tNIAP\tgene\t$temp_sub_loci_region_ref->{$temp_sub_locus}->[0]\t$temp_sub_loci_region_ref->{$temp_sub_locus}->[1]\t.\t$temp_strand\t.\tgene_id \"${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}_${temp_sub_locus}\"\;\n";
			}
			my $temp_transcript_count = 1;
			foreach my $temp_eej (sort keys %{$temp_locus->[2]}){
				foreach my $temp_transcript (@{$temp_locus->[2]->{$temp_eej}}){
					my $temp_eej_new = $temp_transcript->[0]. $temp_eej . $temp_transcript->[1];
					my @temp_eej = split(',', $temp_eej_new);
					my $temp_num_exon = @temp_eej;
					print $temp_gtf_fh "$temp_chr\tNIAP\ttranscript\t$temp_transcript->[0]\t$temp_transcript->[1]\t.\t$temp_strand\t.\tgene_id \"${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}_$temp_sub_loci_ref->{$temp_eej_new}\"\; transcript_id \"${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}.${temp_transcript_count}\"\;\n";
					for(my $i = 0; $i < $temp_num_exon; $i++){
						my $temp_cur_exon_num;
						my @temp_exon = split('-', $temp_eej[$i]);
						#Handle exon number for record at the negative strand
						if($temp_strand eq "-"){
							$temp_cur_exon_num = $temp_num_exon - $i;
						}
						#Handle exon number for record at the positive and no strand
						else{
							$temp_cur_exon_num = $i + 1;
						}
						print $temp_gtf_fh "$temp_chr\tNIAP\texon\t$temp_exon[0]\t$temp_exon[1]\t.\t$temp_strand\t.\tgene_id \"${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}_$temp_sub_loci_ref->{$temp_eej_new}\"\; transcript_id \"${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}.${temp_transcript_count}\"\; exon_number \"$temp_cur_exon_num\"\;\n";
					}
					if($temp_exp){
						print $temp_exp_fh "${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}.${temp_transcript_count}";
						foreach my $temp_exp (@{$temp_transcript->[2]}){
							print $temp_exp_fh "\t$temp_exp";
						}
						print $temp_exp_fh "\n";
					}
					my $temp_id = join(",", keys %{$temp_transcript->[3]});
					my $temp_num_id = keys %{$temp_transcript->[3]};
					my @temp_ends = ();
					foreach my $temp_ends (sort {$temp_transcript->[4]->{$b} <=> $temp_transcript->[4]->{$a}} keys %{$temp_transcript->[4]}){
						my $temp_ends_string = "$temp_ends:$temp_transcript->[4]->{$temp_ends}";
						push(@temp_ends, $temp_ends_string);
					}
					my $temp_ends = join(",", @temp_ends);
					print $temp_id_fh "$temp_chr\t$temp_strand\t${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}_$temp_sub_loci_ref->{$temp_eej_new}\t${temp_prefix}_${temp_chr}_$temp_strand_flag{$temp_strand}_${temp_locus_count}.${temp_transcript_count}\t$temp_id\t$temp_num_id\t$temp_ends\n";
					$temp_transcript_count++;
				}
			}
			$temp_locus_count++;
		}
	}
	close $temp_gtf_fh;
	if($temp_exp){
		close $temp_exp_fh;
	}
	close $temp_id_fh;
	
	return 1;
}





