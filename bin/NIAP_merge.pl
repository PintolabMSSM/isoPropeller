#Get chromosome
sub get_chromosome{
	my ($temp_in) = @_;
	my %temp_chr = ();
	open(my $temp_list_fh, $temp_in) or die "<ERROR> Cannot open $temp_in!\n";
	while(<$temp_list_fh>){
		chomp;
		my $temp_file = $_;
		$temp_file =~ s/\r//;
		open(my $temp_gtf_fh, $temp_file) or die "<ERROR> Cannot open $temp_in!\n";
		GTF:while(<$temp_gtf_fh>){
			chomp;
			my $temp_line = $_;
			$temp_line =~ s/\r//;
			if($temp_line =~ /^#/){
				next GTF;
			}
			my @temp_line = split('\t', $temp_line);
			if(@temp_line != 9){
				print STDERR "<ERROR> $temp_file doesn't seem to be in .gtf format.\n";
				exit;
			}
			unless(exists $temp_chr{$temp_line[0]}){
				$temp_chr{$temp_line[0]} = 1;
			}
		}
		close $temp_gtf_fh;
	}
	close $temp_list_fh;
	my @temp_chr = keys %temp_chr;

	return \@temp_chr;
}

#Count the number of samples
sub count_sample{
	my ($temp_in, $temp_sample_ref) = @_;
	my $temp_num_sample = 0;
	open(my $temp_fh, $temp_in) or die "<ERROR> Cannot open $temp_in!\n";
	while(<$temp_fh>){
		chomp;
		my $temp_file = $_;
		$temp_file =~ s/\r//;
		push(@$temp_sample_ref, $temp_file);
		$temp_num_sample++;
	}
	close $temp_fh;

	return $temp_num_sample;
}

#Parse the list
sub parse_list{
	my ($temp_chr, $temp_in, $temp_exp) = @_;
	my %temp_loci = ();
	my @temp_sample = ();
	my $temp_num_sample = count_sample($temp_in);
	my $temp_cur_sample = 0;
	open(my $temp_fh, $temp_in) or die "<ERROR> Cannot open $temp_in!\n";
	while(<$temp_fh>){
		chomp;
		my $temp_file = $_;
		$temp_file =~ s/\r//;
		parse_gtf($temp_chr, $temp_file, $temp_num_sample, $temp_cur_sample, \%temp_loci, $temp_exp);
		#Sort loci
		foreach my $temp_strand (keys %temp_loci){
			my @temp_merged = merge_loci($temp_num_sample, $temp_loci{$temp_strand});
			@{$temp_loci{$temp_strand}} = @temp_merged;
			#print "$temp_chr\t$temp_strand\t@{$loci_ref{$temp_strand}}\n";
		}
		#Make an increament of the sample counter
		$temp_cur_sample++;
		push(@temp_sample, $temp_file);
	}
	close $temp_fh;

	return (\%temp_loci, \@temp_sample);
}

#Parse a gtf file
sub parse_gtf{
	my ($temp_chr, $temp_gtf, $temp_num_sample, $temp_cur_sample, $temp_loci_ref, $temp_exp) = @_;
	open(my $temp_fh, $temp_gtf) or die "<ERROR> Cannot open $temp_gtf!\n";
	my %temp_transcript = ();
	my $temp_exp_flag = 0;
	while(<$temp_fh>){
		chomp;
		my $temp_line = $_;
		$temp_line =~ s/\r//;
		my @temp_line = split('\t', $temp_line);
		if(($temp_line[0] eq $temp_chr) && (($temp_line[2] eq "transcript") || ($temp_line[2] eq "exon"))){
			my $temp_transcript_id;
			if($temp_line[8] =~ /transcript_id \"([^\"]+)\"/){
				$temp_transcript_id = $1;
			}
			else{
				print STDERR "<ERROR> Cannot find transcript ID in\n$temp_line\n";
				exit;
			}
			unless(exists $temp_transcript{$temp_transcript_id}){
				%{$temp_transcript{$temp_transcript_id}} = ();
				$temp_transcript{$temp_transcript_id}{'chr'} = $temp_line[0];
				$temp_transcript{$temp_transcript_id}{'strand'} = $temp_line[6];
				@{$temp_transcript{$temp_transcript_id}{'exon'}} = ();
				if($temp_exp){
					$temp_transcript{$temp_transcript_id}{'exp'} = 0;
				}
			}
			if(($temp_exp) && ($temp_line[8] =~ /$temp_exp \"([^\"]+)\"/)){
				$temp_transcript{$temp_transcript_id}{'exp'} = $1;
				$temp_exp_flag = 1;
			}
			if($temp_line[2] eq "exon"){
				my @temp_exon = ($temp_line[3], $temp_line[4]);
				push(@{$temp_transcript{$temp_transcript_id}{'exon'}}, \@temp_exon);
			}
		}
	}
	close $temp_fh;
	if($temp_exp_flag == 0){
		print STDERR "<WARNING> Invalid attribute $temp_exp, no expression data will be output for $temp_chr in $temp_gtf.\n";
	}
	parse_transcript(\%temp_transcript, $temp_num_sample, $temp_cur_sample, $temp_loci_ref);

	return 1;
}

#Parse the transcripts
sub parse_transcript{
	my ($temp_transcript_ref, $temp_num_sample, $temp_cur_sample, $temp_loci_ref) = @_;
	foreach my $temp_transcript_id (keys %{$temp_transcript_ref}){
		#Extract the information of the current transcript for simplicity
		my $temp_strand = $temp_transcript_ref->{$temp_transcript_id}{'strand'};
		my $temp_exp = $temp_transcript_ref->{$temp_transcript_id}{'exp'};
		my @temp_exon = @{$temp_transcript_ref->{$temp_transcript_id}{'exon'}};
		#Parse the exon structure
		my @temp_sorted_exon = sort {$a->[0] <=> $b->[0]} @temp_exon;
		my $temp_exon_chain = $temp_sorted_exon[0][0] . "-" . $temp_sorted_exon[0][1];
		for(my $i = 1; $i < @temp_sorted_exon; $i++){
			$temp_exon_chain .= "," . $temp_sorted_exon[$i][0] . "-" . $temp_sorted_exon[$i][1];
		}
		my @temp_eej = split('-', $temp_exon_chain);
		my $temp_leftmost = shift @temp_eej;
		my $temp_rightmost = pop @temp_eej;
		my $temp_eej = "-" . join("-", @temp_eej) . "-";
		$temp_eej =~ s/--/-/;
		#print "$temp_chr\t$temp_strand\t$temp_exp\t$temp_exon_chain\n$temp_leftmost\t$temp_rightmost\t$temp_eej\t$temp_transcript_id\n";
		add_transcript($temp_loci_ref, $temp_num_sample, $temp_cur_sample, $temp_strand, $temp_exp, $temp_leftmost, $temp_rightmost, $temp_eej, $temp_transcript_id);
	}

	return 1;
}

