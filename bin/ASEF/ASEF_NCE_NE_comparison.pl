#!/usr/bin/env perl
=start
Author: ALin
Purpose: This is a script for finding novel exons genearted by ASEF_NE_vx.pl corresponding to each novel coding exons generated by ASEF_NCE_vx.pl. It is part of the Alternative Splicing Event Finder (ASEF) package.
Change log:
	v1.2	2023-12-21	Restructured the code. Fixed an issue in parsing gtf attribute.
=cut
use strict;
use Getopt::Long;
use threads;
use Benchmark qw(:hireswallclock);

my $total_start_time = Benchmark->new;

my $version = "v1.2";

my $usage="Usage: perl ASEF_NCE_NE_comparison_${version}.pl
	-c <String> .gtf of novel coding exons
	-e <String> .gtf of novel exons
	-o <String> Output
	-t <Integer> Number of threads
	-b <String> Path for bedtools
	-h <Boolean> Help
";

my ($nce, $ne, $out, $num_thread, $btp, $help) = ("", "", "", 1, "", 0);

GetOptions(
	'c=s'	=>	\$nce,
	'e=s'	=>	\$ne,
	'o=s'	=>	\$out,
	't=i'	=>	\$num_thread,
	'b=s'	=>	\$btp,
	'h!'    =>      \$help
);

unless($nce && $ne && $out){
	print "$usage";
	exit;
}

if($btp eq ""){
	$btp = "bedtools";
}

unless(`$btp`){
	print "Incorrect path for bedtools!\n$usage";
	exit;
}

if($help){
	print "$usage";
	exit;
}

my $date_format = "+%F-%T.%3N";
my $date = `date $date_format`;
chomp $date;
print "[$date]\tperl ASEF_NCE_NE_comparison_${version}.pl -c $nce -e $ne -o $out -b $btp -t $num_thread\n";

my $dir = "${out}__ASEF_NCE_NE_comparison_temp";
if(-d $dir){
	print STDERR "<WARNING> Temporary directory $dir exists! Will overwrite it...\n";
	system("rm ${dir}/*");
}
else{
	system("mkdir $dir");
}

my $total_thread = `grep -c \"^processor\" /proc/cpuinfo`;
chomp $total_thread;
$date = `date $date_format`;
chomp $date;
print "[$date]\tA total of $total_thread threads detected\n";

if($num_thread > $total_thread){
	print "<WARNING> Exceeding the maximal total number of threads! Setting the number of thread to $total_thread...";
	$num_thread = $total_thread;
}

my $start_time = Benchmark->new;
$date = `date $date_format`;
chomp $date;
print "[$date]\tStarting preprocessing...\n";
my $chr_ref = split_gtf($ne, $nce, $dir);
my $finish_time = Benchmark->new;
my $time_spent = timediff($finish_time, $start_time);
$date = `date $date_format`;
chomp $date;
print "[$date]\tUsed ". timestr($time_spent) . " for preprocessing\n";

my $chr_pos = 0;
my @thread = ();
my @running = ();

while(@thread < @$chr_ref){
	@running = threads->list(threads::running);
	if(@running < $num_thread){
		my $thread = threads->create(\&worker, $chr_ref->[$chr_pos], $dir, $date_format, $btp);
		push(@thread, $thread);
		$chr_pos++;
	}
}

foreach my $thread (@thread){
	my @result = $thread->join();
	$date = `date $date_format`;
	chomp $date;
	print "[$date]\tJoined for $result[0]!\n";
}

my $out_fh;
open($out_fh, "> $out") or die "<ERROR> Cannot create $out!\n";
print $out_fh "transcript_id\tchr\tstrand\tleft\tright\tnovel_cds_type\tNOE\tNFD\tNFE\tNLA\tNLE\tNDIE\tNAIE\tKEIE\tNEIE\n";
close $out_fh;
system("cat ${dir}/*_out.txt | sort -k 2,2 -k 4,4n -k 1,1 >> $out");
$date = `date $date_format`;
chomp $date;
print "[$date]\tGenerated $out!\n";
system("rm -r $dir");
$date = `date $date_format`;
chomp $date;
print "[$date]\tCleaned temporary files!\n";

my $total_finish_time = Benchmark->new;
my $total_time_spent = timediff($total_finish_time, $total_start_time);
$date = `date $date_format`;
chomp $date;
print "[$date]\tTotal time used: ". timestr($total_time_spent) . "\n";

sub worker{
	my ($temp_chr, $temp_dir, $temp_date_format, $temp_btp) = @_;
	my $temp_date = `date $temp_date_format`;
	chomp $temp_date;
	print "[$temp_date]\tStarting to process $temp_chr...\n";
	my $temp_start_time = Benchmark->new;
	my $temp_comaprison_ref = compare_nce_ne("${temp_dir}/${temp_chr}_nce.gtf", "${temp_dir}/${temp_chr}_ne.gtf", $temp_btp);
	print_result($temp_chr, $temp_comaprison_ref, "${temp_dir}/${temp_chr}_out.txt");
	my $temp_finish_time = Benchmark->new;
	my $temp_time_spent = timediff($temp_finish_time, $temp_start_time);
	$temp_date = `date $temp_date_format`;
	chomp $temp_date;
	print "[$temp_date]\tDone for $temp_chr in " . timestr($temp_time_spent) , "!\n";
	
	return $temp_chr;
}

sub split_gtf{
	my ($temp_ne, $temp_nce, $temp_dir) = @_;
	system("cut -f1 $temp_nce | sort | uniq > ${temp_dir}/chr_list.txt");
	my @temp_chr = ();
	my $temp_fh;
	open($temp_fh, "${temp_dir}/chr_list.txt") or die "<ERROR> Cannot open ${temp_dir}/chr_list.txt!\n";
	while(<$temp_fh>){
		chomp;
		my $temp_line = $_;
		push(@temp_chr, $temp_line);
	}
	close $temp_fh;
	system("for i in `cat ${temp_dir}/chr_list.txt`; do grep \"^\$i	\" $temp_ne > ${temp_dir}/\$\{i\}_ne.gtf; done");
	system("for i in `cat ${temp_dir}/chr_list.txt`; do grep \"^\$i	\" $temp_nce > ${temp_dir}/\$\{i\}_nce.gtf; done");

	return \@temp_chr;
}

sub compare_nce_ne{
	my ($temp_nce, $temp_ne, $temp_btp) = @_;
	my %temp_comparison = ();
	my ($temp_intersect_fh, $temp_out_fh);
	open($temp_intersect_fh, "$temp_btp intersect -wao -s -nonamecheck -a $temp_nce -b $temp_ne |") or die "<ERROR> Cannot intersect $temp_nce and $temp_ne!\n";
	while(<$temp_intersect_fh>){
		chomp;
		my $temp_line = $_;
		$temp_line =~ s/\r//;
		my @temp_line = split('\t', $temp_line);
		if($temp_line[2] ne 'CDS'){
			print STDERR "<ERROR> The -c argument could be incorrect!\n";
			exit;
		}
		my $temp_transcript_id;
		if($temp_line[8] =~ /(^|\s)transcript_id \"([^\"]+)\"/){
			$temp_transcript_id = $2;
		}
		else{
			print STDERR "<ERROR> Missing transcript_id at line:\n$temp_line\n";
			exit;
		}
		my $temp_cds = "$temp_line[3]-$temp_line[4]";
		my $temp_novel_cds_type;
		if($temp_line[8] =~ /(^|\s)tag \"([^\"]+)\"/){
			$temp_novel_cds_type = $2;
		}
		else{
			print STDERR "<ERROR> The -c argument could be incorrect!\n";
			exit;
		}
		unless(exists $temp_comparison{$temp_transcript_id}){
			%{$temp_comparison{$temp_transcript_id}} = ();
		}
		unless($temp_comparison{$temp_transcript_id}{$temp_cds}){
			%{$temp_comparison{$temp_transcript_id}{$temp_cds}} = ();
			$temp_comparison{$temp_transcript_id}{$temp_cds}{'strand'} = $temp_line[6];
			$temp_comparison{$temp_transcript_id}{$temp_cds}{'novel_cds_type'} = $temp_novel_cds_type;
			for my $temp_col ("NOE", "NFD", "NFE", "NLA", "NLE", "NDIE", "NAIE", "KEIE", "NEIE"){
				$temp_comparison{$temp_transcript_id}{$temp_cds}{$temp_col} = 0;
			}
		}
		if($temp_line[18] > 0){
			if($temp_line[11] ne 'exon'){
				print STDERR "<ERROR> The -ne argument could be incorrect!\n";
				exit;
			}
			if($temp_line[17] =~ /(^|\s)transcript_id \"$temp_transcript_id\"/){
				for my $temp_col ("NOE", "NFD", "NFE", "NLA", "NLE", "NDIE", "NAIE", "KEIE", "NEIE"){
					if($temp_line[17] =~ /(^|\s)tag \"$temp_col\"/){
						$temp_comparison{$temp_transcript_id}{$temp_cds}{$temp_col} = 1;
					}
				}
			}
		}
	}
	
	return \%temp_comparison;
}

sub print_result{
	my ($temp_chr, $temp_comparison_ref, $temp_out) = @_;
	my $temp_fh;
	open($temp_fh, "> $temp_out") or die "<ERROR> Cannot create $temp_out!\n";
	foreach my $temp_transcript_id (keys %$temp_comparison_ref){
		foreach my $temp_cds (keys %{$temp_comparison_ref->{$temp_transcript_id}}){
			my @temp_cds = split('-', $temp_cds);
			print $temp_fh "$temp_transcript_id\t$temp_chr\t$temp_comparison_ref->{$temp_transcript_id}->{$temp_cds}->{'strand'}\t$temp_cds[0]\t$temp_cds[1]\t$temp_comparison_ref->{$temp_transcript_id}->{$temp_cds}->{'novel_cds_type'}";
			for my $temp_col ("NOE", "NFD", "NFE", "NLA", "NLE", "NDIE", "NAIE", "KEIE", "NEIE"){
				print $temp_fh "\t$temp_comparison_ref->{$temp_transcript_id}->{$temp_cds}->{$temp_col}";
			}
			print $temp_fh "\n";	
		}
	}
	close $temp_fh;

	return 1;
}



