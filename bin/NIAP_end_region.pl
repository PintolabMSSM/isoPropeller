#!/usr/bin/env perl
=start
Author: ALin
Purpose: To generate TSS/TTS regions based on 5' and 3' ends distance, as well as the *_id.txt file generated by NIAP_merge_x.pl.
Change log:
	v1.2	2024-09	Fixed the output bed left coordinate.
	v1.3	2025-03	Fixed an issue to proper error reporting when reading the ID file.
=cut

use strict;
use Getopt::Long;
use List::Util qw(max min);
use threads;
use Benchmark qw(:hireswallclock);

my $total_start_time = Benchmark->new;

my $version = "v1.3";

my ($in, $out, $id, $num_thread, $help) = ("", "", "", 1, 0);

my $usage = "Usage: perl NIAP_end_region_${version}.pl
	-i <String> Input list of _end_dist.txt file(s)
	-o <String> Output base
	-d <String> The _id.txt file generated by NIAP_merge_*.pl
	-t <Integer> The number of threads to use (Default: $num_thread)
	-h <Boolean> Help
";

GetOptions(
	'i=s'	=>	\$in,
	'o=s'	=>	\$out,
	'd=s'	=>	\$id,
	't=i'	=>	\$num_thread,
        'h!'    =>      \$help,
);

if($help){
        print "$usage";
        exit;
}

unless($in && $out && $id){
	print "$usage";
	exit;
}

my $date_format = "+%F-%T.%3N";
my $date = `date $date_format`;
chomp $date;
print "[$date]\tperl NIAP_end_region_${version}.pl -i $in -o $out -d $id -t $num_thread\n";

my $dir = "${out}__NIAP_end_region_temp";
if(-d $dir){
	print STDERR "<WARNNING> Temporary directory $dir exists! Will overwrite it...\n";
	system("rm ${dir}/*");
}
else{
	system("mkdir $dir");
}

my $total_thread = `grep -c \"^processor\" /proc/cpuinfo`;
chomp $total_thread;
$date = `date $date_format`;
chomp $date;
print "[$date]\tA total of $total_thread threads detected\n";

if($num_thread > $total_thread){
	print "<WARNING> Exceeding the maximal total number of threads! Setting the number of thread to $total_thread...";
	$num_thread = $total_thread;
}

my $start_time = Benchmark->new;
$date = `date $date_format`;
chomp $date;
print "[$date]\tStarting preprocessing...\n";
my $chr_ref = split_chromosome($dir, $id);
my $finish_time = Benchmark->new;
my $time_spent = timediff($finish_time, $start_time);
$date = `date $date_format`;
chomp $date;
print "[$date]\tUsed ". timestr($time_spent) . " for preprocessing\n";

my $chr_pos = 0;
my @thread = ();
my @running = ();

while(@thread < @$chr_ref){
	@running = threads->list(threads::running);
	if(@running < $num_thread){
		my $thread = threads->create(\&worker, $chr_ref->[$chr_pos], $dir, $date_format, $in);
		push(@thread, $thread);
		$chr_pos++;
	}
}

foreach my $thread (@thread){
	my @result = $thread->join();
	$date = `date $date_format`;
	chomp $date;
	print "[$date]\tJoined for $result[0]!\n";
}

system("cat ${dir}/*_tss.bed | sort -k 1,1 -k 2,2n > ${out}_tss.bed");
system("cat ${dir}/*_tts.bed | sort -k 1,1 -k 2,2n > ${out}_tts.bed");
$date = `date $date_format`;
chomp $date;
print "[$date]\tGenerated output file!\n";
system("rm -r ${dir}");
$date = `date $date_format`;
chomp $date;
print "[$date]\tCleaned temporary files!\n";

my $total_finish_time = Benchmark->new;
my $total_time_spent = timediff($total_finish_time, $total_start_time);
$date = `date $date_format`;
chomp $date;
print "[$date]\tTotal time used: ". timestr($total_time_spent) . "\n";

sub worker{
	my ($temp_chr, $temp_dir, $temp_date_format, $temp_in) = @_;
	my $temp_date = `date $temp_date_format`;
	chomp $temp_date;
	print "[$temp_date]\tStarting to process $temp_chr...\n";
	my $temp_start_time = Benchmark->new;
	my ($temp_id_ref) = read_id("${temp_dir}/${temp_chr}_id.txt");
	my ($temp_ends_ref) = parse_list($temp_chr, $temp_id_ref, $temp_in);
	$temp_date = `date $temp_date_format`;
	chomp $temp_date;
	print "[$temp_date]\tParsing file list done for $temp_chr!\n";
	print_bed($temp_ends_ref, $temp_chr, $temp_dir);
	my $temp_finish_time = Benchmark->new;
	my $temp_time_spent = timediff($temp_finish_time, $temp_start_time);
	$temp_date = `date $temp_date_format`;
	chomp $temp_date;
	print "[$temp_date]\tDone for $temp_chr in " . timestr($temp_time_spent) , "!\n";

	return $temp_chr;
}

#Split chromosome
sub split_chromosome{
	my ($temp_dir, $temp_id) = @_;
	my %temp_chr = ();
	open(my $temp_fh, $temp_id) or die "<ERROR> Cannot open $temp_id!\n";
	my $temp_header = 0;
	my %temp_header = ();
	while(<$temp_fh>){
		chomp;
		my $temp_line = $_;
		$temp_line =~ s/\r//;
		my @temp_line = split('\t', $temp_line);
		if($temp_header == 0){
			for(my $i = 0; $i < @temp_line; $i++){
				if(exists $temp_header{$temp_line[$i]}){
					print STDERR "<ERROR> Duplicated column $temp_line[$i] in $temp_id!\n";
					exit;
				}
				else{
					$temp_header{$temp_line[$i]} = $i;
				}
			}
			unless(exists $temp_header{'chr'}){
				print STDERR "<ERROR> chr absent in $temp_id!\n";
				exit;
			}
			unless(exists $temp_header{'transcript_id'}){
				print STDERR "<ERROR> transcript_id absent in $temp_id!\n";
				exit;
			}
			unless(exists $temp_header{'original_id'}){
				print STDERR "<ERROR> original_id absent in $temp_id!\n";
				exit;
			}
			$temp_header = 1;
		}
		else{
			unless(exists $temp_chr{$temp_line[$temp_header{'chr'}]}){
				$temp_chr{$temp_line[$temp_header{'chr'}]} = 1;
			}
		}
	}
	close $temp_fh;
	my @temp_chr = keys %temp_chr;
	foreach my $temp_chr (@temp_chr){
		system("head -1 $temp_id > ${temp_dir}/${temp_chr}_id.txt");
		system("grep \"^$temp_chr\"\$\'\\t\' $temp_id >> ${temp_dir}/${temp_chr}_id.txt");
	}

	return \@temp_chr;
}

#Read the _id.txt file
sub read_id{
	my ($temp_id) = @_;
	open(my $temp_fh, $temp_id) or die "Cannot open $temp_id!\n";
	my %temp_id = ();
	my $temp_header = 0;
	my %temp_header = ();
	while(<$temp_fh>){
		chomp;
		my $temp_line = $_;
		$temp_line =~ s/\r//;
		my @temp_line = split('\t', $temp_line);
		if($temp_header == 0){
			for(my $i = 0; $i < @temp_line; $i++){
				if(exists $temp_header{$temp_line[$i]}){
					print STDERR "<ERROR> Duplicated column $temp_line[$i] in $temp_id!\n";
					exit;
				}
				else{
					$temp_header{$temp_line[$i]} = $i;
				}
			}
			unless(exists $temp_header{'chr'}){
				print STDERR "<ERROR> chr absent in $temp_id!\n";
				exit;
			}
			unless(exists $temp_header{'transcript_id'}){
				print STDERR "<ERROR> transcript_id absent in $temp_id!\n";
				exit;
			}
			unless(exists $temp_header{'original_id'}){
				print STDERR "<ERROR> original_id absent in $temp_id!\n";
				exit;
			}
			$temp_header = 1;
		}
		else{
			my @temp_original_id = split(',', $temp_line[$temp_header{'original_id'}]);
			foreach my $temp_original_id (@temp_original_id){
				if(exists $temp_id{$temp_original_id}){
					print STDERR "<ERROR> Duplicated ID $temp_original_id in $temp_id!\n";
					exit;
				}
				else{
					$temp_id{$temp_original_id} = $temp_line[$temp_header{'transcript_id'}];
				}
			}
		}
	}
	close $temp_fh;

	return (\%temp_id);
}

#Parse the list
sub parse_list{
	my ($temp_chr, $temp_id_ref, $temp_in)= @_;
	open(my $temp_fh, $temp_in) or die "<ERROR> Cannot open $temp_in!\n";
	my %temp_ends = ();
	while(<$temp_fh>){
		chomp;
		my $temp_file = $_;
		$temp_file =~ s/\r//;
		parse_end_dist($temp_chr, \%temp_ends, $temp_id_ref, $temp_file);
	}
	close $temp_fh;

	return (\%temp_ends);
}

#Parse a end_dist.txt file
sub parse_end_dist{
	my ($temp_chr, $temp_ends_ref, $temp_id_ref, $temp_in) = @_;
	open(my $temp_fh, $temp_in) or die "<ERROR> Cannot open $temp_in!\n";
	my %temp_header = ();
	my $temp_header = 0;
	while(<$temp_fh>){
		chomp;
		my $temp_line = $_;
		$temp_line =~ s/\r//;
		my @temp_line = split('\t', $temp_line);
		if($temp_header == 0){
			for(my $i = 0; $i < @temp_line; $i++){
				if(exists $temp_header{$temp_line[$i]}){
					print STDERR "<ERROR> Duplicated column $temp_line[$i] in $temp_in!\n";
					exit;
				}
				else{
					$temp_header{$temp_line[$i]} = $i;
				}
			}
			unless(exists $temp_header{'chr'}){
				print STDERR "<ERROR> chr absent in $temp_in!\n";
				exit;
			}
			unless(exists $temp_header{'transcript_id'}){
				print STDERR "<ERROR> transcript_id absent in $temp_in!\n";
				exit;
			}
			unless(exists $temp_header{'depth'}){
				print STDERR "<ERROR> depth absent in $temp_in!\n";
				exit;
			}
			unless(exists $temp_header{'end_dist'}){
				print STDERR "<ERROR> end_dist absent in $temp_in!\n";
				exit;
			}
			$temp_header = 1;
		}
		else{
			if(($temp_line[$temp_header{'chr'}] eq $temp_chr) && (exists $temp_id_ref->{$temp_line[$temp_header{'transcript_id'}]})){
				my $temp_transcript_id = $temp_id_ref->{$temp_line[$temp_header{'transcript_id'}]};
				my $temp_strand = $temp_line[$temp_header{'strand'}];
				unless(exists $temp_ends_ref->{$temp_transcript_id}){
					%{$temp_ends_ref->{$temp_transcript_id}} = ();
					$temp_ends_ref->{$temp_transcript_id}{'strand'} = $temp_strand;
				}
				my @temp_end_dist = split(',', $temp_line[$temp_header{'end_dist'}]);
				foreach my $temp_end_dist (@temp_end_dist){
					my ($temp_tss, $temp_tts);
					$temp_end_dist =~ /^(\d+)-(\d+):/;
					if($temp_strand eq '-'){
						($temp_tss, $temp_tts) = ($2, $1);
					}
					else{
						($temp_tss, $temp_tts) = ($1, $2);
					}
					if(exists $temp_ends_ref->{$temp_transcript_id}{'tss'}){
						if($temp_ends_ref->{$temp_transcript_id}{'tss'}[0] > $temp_tss){
							$temp_ends_ref->{$temp_transcript_id}{'tss'}[0] = $temp_tss;
						}
						if($temp_ends_ref->{$temp_transcript_id}{'tss'}[1] < $temp_tss){
							$temp_ends_ref->{$temp_transcript_id}{'tss'}[1] = $temp_tss;
						}
					}
					else{
						@{$temp_ends_ref->{$temp_transcript_id}{'tss'}} = ($temp_tss, $temp_tss);
					}
					if(exists $temp_ends_ref->{$temp_transcript_id}{'tts'}){
						if($temp_ends_ref->{$temp_transcript_id}{'tts'}[0] > $temp_tts){
							$temp_ends_ref->{$temp_transcript_id}{'tts'}[0] = $temp_tts;
						}
						if($temp_ends_ref->{$temp_transcript_id}{'tts'}[1] < $temp_tts){
							$temp_ends_ref->{$temp_transcript_id}{'tts'}[1] = $temp_tts;
						}
					}
					else{
						@{$temp_ends_ref->{$temp_transcript_id}{'tts'}} = ($temp_tts, $temp_tts);
					}
				}

			}
		}
	}
	close $temp_fh;

	return 1;
}

#Print TSS/TTS bed
sub print_bed{
	my ($temp_ends_ref, $temp_chr, $temp_dir) = @_;
	open(my $temp_tss_fh, "> ${temp_dir}/${temp_chr}_tss.bed") or die "<ERROR> Cannot create ${temp_dir}/${temp_chr}_tss.bed!\n";
	open(my $temp_tts_fh, "> ${temp_dir}/${temp_chr}_tts.bed") or die "<ERROR> Cannot create ${temp_dir}/${temp_chr}_tts.bed!\n";
	foreach my $temp_transcript_id (keys %$temp_ends_ref){
		$temp_ends_ref->{$temp_transcript_id}{'tss'}[0]--;
		print $temp_tss_fh "$temp_chr\t$temp_ends_ref->{$temp_transcript_id}{'tss'}[0]\t$temp_ends_ref->{$temp_transcript_id}{'tss'}[1]\t$temp_transcript_id\t.\t$temp_ends_ref->{$temp_transcript_id}{'strand'}\n";
		$temp_ends_ref->{$temp_transcript_id}{'tts'}[0]--;
		print $temp_tts_fh "$temp_chr\t$temp_ends_ref->{$temp_transcript_id}{'tts'}[0]\t$temp_ends_ref->{$temp_transcript_id}{'tts'}[1]\t$temp_transcript_id\t.\t$temp_ends_ref->{$temp_transcript_id}{'strand'}\n";
	}
	close $temp_tss_fh;
	close $temp_tts_fh;
	
	return 1;
}





