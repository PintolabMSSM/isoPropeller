use strict;

my $version = "v1.5";

my $usage = "Usage: perl classification_conversion.pl <input> <output>\n";

if(@ARGV != 2){
	print STDERR "$usage";
	exit;
}

my ($in, $out) = @ARGV;

unless($in && $out){
	print STDERR "$usage";
	exit;
}

classification_conversion($in, $out);

sub classification_conversion{
	my ($temp_in, $temp_out) = @_;
	open(my $temp_in_fh, $temp_in) or die "Cannot open $temp_in!\n";
	open(my $temp_out_fh, "> $temp_out") or die "Cannot create $temp_out!\n";
	my $temp_header = 0;
	my %temp_header = ();
	print $temp_out_fh "transcript_id\tstatus\tisoPropeller_structural_category\tisoPropeller_structural_subcategory\n";
	while(<$temp_in_fh>){
		chomp;
		my $temp_line = $_;
		$temp_line =~ s/\r//;
		my @temp_line = split('\t', $temp_line);
		if($temp_header == 0){
			for(my $i = 0; $i < @temp_line; $i++){
				if(exists $temp_header{$temp_line[$i]}){
					print STDERR "Duplicated column $temp_line[$i] in $temp_in!\n";
					exit;
				}
				else{
					$temp_header{$temp_line[$i]} = $i;
				}
			}
			$temp_header = 1;
		}
		else{
			my ($temp_transcript_id, $temp_status, $temp_isop_sc, $temp_isop_ssc) = ($temp_line[$temp_header{'transcript_id'}], $temp_line[$temp_header{'status'}], "", "");
			if($temp_status eq 'known'){
				$temp_isop_sc = $temp_status;
				$temp_isop_ssc = $temp_status;
			}
			elsif($temp_status =~ /partial/){
				$temp_isop_sc = "partial";
				$temp_isop_ssc = $temp_status;
			}
			elsif($temp_status eq 'novel_structure'){
				$temp_isop_sc = $temp_status;
				if($temp_line[$temp_header{'exon'}] == 1){
					$temp_isop_ssc = "monoexonic";
				}
				else{
					my $temp_nss_num = $temp_line[$temp_header{'NFD'}] + $temp_line[$temp_header{'NLA'}] + $temp_line[$temp_header{'NDIE'}] + $temp_line[$temp_header{'NAIE'}] + $temp_line[$temp_header{'NEIE'}];
					if($temp_nss_num == 0){
						$temp_isop_ssc = "novel_in_catalog";
					}
					else{
						$temp_isop_ssc = "novel_not_in_catalog";
					}
				}
			}
			elsif(($temp_status eq 'ncRNA_host_gene') || ($temp_status eq 'divergent') || ($temp_status eq 'intronic') || ($temp_status eq 'overlapping') || ($temp_status eq 'antisense')){
				$temp_isop_sc = "novel_gene";
				$temp_isop_ssc = $temp_status;
			}
			elsif($temp_status eq 'intergenic'){
				$temp_isop_sc = "novel_gene";
				if($temp_line[$temp_header{'ref_gene_id'}] eq ''){
					$temp_isop_ssc = "intergenic_no_overlap";
				}
				else{
					$temp_isop_ssc = "intergenic_non_pc_overlap";
				}
			}
			elsif($temp_status eq 'fusion'){
				$temp_isop_sc = "fusion";
				my @temp_gene_id = split('\|', $temp_line[$temp_header{'gene_id'}]);
				if($temp_line[$temp_header{'reclocus'}] ne ''){
					@temp_gene_id = split('\|', $temp_line[$temp_header{'reclocus'}]);
				}
				my $temp_known_num = 0;
				foreach my $temp_gene_id (@temp_gene_id){
					if($temp_gene_id =~ /^ENSG/){
						$temp_known_num++;
					}
				}
				if(@temp_gene_id == $temp_known_num){
					$temp_isop_ssc = "fusion_known";
				}
				elsif($temp_known_num > 0){
					$temp_isop_ssc = "fusion_mixed";
				}
				else{
					$temp_isop_ssc = "fusion_novel";
				}
			}
			print $temp_out_fh "$temp_transcript_id\t$temp_status\t$temp_isop_sc\t$temp_isop_ssc\n";
		}
	}
	close $temp_in_fh;
	close $temp_out_fh;

	return 1;
}





